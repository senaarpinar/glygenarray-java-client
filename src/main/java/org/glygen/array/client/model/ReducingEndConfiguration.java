package org.glygen.array.client.model;

public class ReducingEndConfiguration {
    
    ReducingEndType type;
    String comment;
    /**
     * @return the type
     */
    public ReducingEndType getType() {
        return type;
    }
    /**
     * @param type the type to set
     */
    public void setType(ReducingEndType type) {
        this.type = type;
    }
    /**
     * @return the comment
     */
    public String getComment() {
        return comment;
    }
    /**
     * @param comment the comment to set
     */
    public void setComment(String comment) {
        this.comment = comment;
    }

}
